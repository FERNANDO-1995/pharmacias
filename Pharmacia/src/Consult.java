    private void SelecionarP() {
        Double fechament = 0.0;
        if ("    /  /  ".equalsIgnoreCase(Datavendas.getText())) {
            JOptionPane.showMessageDialog(null, "ADD A DATA PRIMEIRO");
        } else {
       
        sql = "SELECT cliente.nome as clnome, "
                + "produto.nome as nom,produto.preco,"
                + "produto.tipo_medicamento,saidas.qtdade,saidas.total,saidas.data,funcionario.nome as fnome FROM saidas join"
                + " produto  on saidas.producto_id = produto.id  && saidas.data='"+Datavendas.getText()+"' && saidas.funcionario_id='"+id+"' "
                + "join cliente on cliente.id=saidas.cliente_id join funcionario on funcionario.id='" + id+ "' "
                + " ORDER BY saidas.id DESC";
        DefaultTableModel dtm = (DefaultTableModel) tbpesquisa.getModel();
        if (dtm.getRowCount() != 0) {

            ((DefaultTableModel) tbpesquisa.getModel()).setNumRows(0);// serve para formatar a tualiza a tabela e vai repor as linhas que tiver dentro da tabela que esta no java
            tbpesquisa.updateUI();

        }
int i =1;
        try (PreparedStatement ps = (PreparedStatement) conexao.conectar().prepareStatement(sql);
                ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                fechament+=rs.getInt(6);
                Object[] recebeDado = {i++,rs.getString(2), rs.getString(4),
                    rs.getString(3),  rs.getString(5), rs.getString(6), rs.getString(7), rs.getString(1)};
                dtm.addRow(recebeDado);

            }
            fechamento.setText("Fechamento: "+fechament+"0");

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    }

