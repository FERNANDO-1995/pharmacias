    private void SelecionarM() {
        sql = "select * from funcionario where nome like '" + TXTPESQUISAR.getText().toUpperCase() + "%' ORDER BY id desc";
        DefaultTableModel dtm = (DefaultTableModel) tbfuncionario.getModel();
        if (dtm.getRowCount() != 0) {

            ((DefaultTableModel) tbfuncionario.getModel()).setNumRows(0);// serve para formatar a tualiza a tabela e vai repor as linhas que tiver dentro da tabela que esta no java
            tbfuncionario.updateUI();

        }
        tbfuncionario.setAutoResizeMode(tbfuncionario.AUTO_RESIZE_OFF);
tbfuncionario.getColumnModel().getColumn(0).setPreferredWidth(34);
tbfuncionario.getColumnModel().getColumn(1).setPreferredWidth(200);
tbfuncionario.getColumnModel().getColumn(3).setPreferredWidth(110);
        try (PreparedStatement ps = (PreparedStatement) conexao.conectar().prepareStatement(sql);
                ResultSet rs = ps.executeQuery()) {
            while (rs.next()) {
                Object[] recebeDado = {rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(8)
                , rs.getString(7), rs.getString(9), rs.getString(5)};
                dtm.addRow(recebeDado);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
