    private void SalvarFuncionario() {
        sql = "insert into funcionario values(null,'" + nomefuncionario.getText().toUpperCase() + "', "
                + "'" + telefonefuncionario.getText().toUpperCase() + "'"
                + ",'" + bifuncionario.getText().toUpperCase() + "','" + senhafuncionario.getText() + "'"
                + ", '" + moradafuncioanrio.getText().toUpperCase() + "',"
                + "'" + funcaofuncionariio.getSelectedItem() + "','" + generofuncionario.getSelectedItem() + "',"
                + " '" + estadofuncionario.getSelectedItem() + "')";

        try {
            conexao.conectar();
            int a = conexao.cadastrar_dado(sql);
            if (a != 0) {
                JOptionPane.showMessageDialog(null, "REGISTO CADASTRADO COM SUCESSO");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    private void AtualizarFuncionario() {

        sql = "update funcionario set nome = '" + nomefuncionario.getText().toUpperCase() + "'"
                + ", telefone ='" + telefonefuncionario.getText().toUpperCase() + "'"
                + ", bi ='" + bifuncionario.getText().toUpperCase() + "',"
                + " sexo ='" + generofuncionario.getSelectedItem() + "', "
                + "estado ='" + estadofuncionario.getSelectedItem() + "',senha ='" + senhafuncionario.getText() + "',"
                + "  cargo = '" + funcaofuncionariio.getSelectedItem() + "'  where id = '" + idfuncionario + "'";

        try {
            conexao.conectar();
            int verificar = conexao.cadastrar_dado(sql);
            if (verificar != 0) {
                JOptionPane.showMessageDialog(null, "REGISTO ATUALIZADO COM SUCESSO");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

